import { NextResponse } from 'next/server'
import { parseDialogue } from '@/lib/parseDialogue'

export async function POST(request: Request) {
  try {
    const formData = await request.formData()
    const file = formData.get('file') as File
    if (!file) return NextResponse.json({ error: '파일이 없습니다.' }, { status: 400 })
    const text = await file.text()
    const record = parseDialogue(text, file.name.replace('.txt', ''))
    const totalLines = record.phases.reduce((ps, phase) =>
      ps + phase.sections.reduce((ss, section) => ss + section.lines.length, 0), 0)
    if (totalLines === 0) return NextResponse.json({ error: '파싱된 대화가 없습니다.' }, { status: 400 })
    return NextResponse.json(record)
  } catch (error) {
    return NextResponse.json({ error: '파일 처리 중 오류' }, { status: 500 })
  }
}
